// Сумма цифр в тексте

// Составить функцию, которая преобразует текущий символ цифры
// в число. Написать программу считающую сумму цифр в тексте.
// int digit_to_num(char c)
// Input format
// Строка состоящая из английских букв, пробелов, знаков
// препинания. В конце строки символ точка.
// Output format
// Целое число - сумма цифр в тексте

#include <stdio.h>

// Преобразует символ цифры в число: '0' -> 0, '1' -> 1, ..., '9' -> 9
int digit_to_num(char c)
{
    return c - '0';
}

// Вспомогательная функция: проверяет, является ли символ цифрой
int is_digit(char c)
{
    return (c >= '0' && c <= '9');
}

int main()
{
    char ch;
    int sum = 0;

    while ((ch = getchar()) != '.')
    {
        if (is_digit(ch))
        {
            sum += digit_to_num(ch);
        }
    }

    printf("%d\n", sum);
    return 0;
}