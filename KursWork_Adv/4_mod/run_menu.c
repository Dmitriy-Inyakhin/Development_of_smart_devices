/* ==================================================================================
 * Функция: show_status_bar
 * ==================================================================================
 * Назначение:
 *   Отображает информационную строку состояния в нижней части экрана с
 *   текущими значениями всех настроек программы.
 *
 * Параметры:
 *   state — указатель на структуру AppState с текущим состоянием программы
 *
 * Возвращаемое значение:
 *   Нет (void)
 *
 * Особенности:
 *   - Занимает 5 строк в нижней части экрана
 *   - Отображает:
 *       • Выбранный метод расчёта (или "Не выбран")
 *       • Первую функцию (или "Не выбрана")
 *       • Вторую функцию (или "Не выбрана")
 *       • Границы интервала в формате [%.8f, %.8f]
 *       • Значение эпсилон в экспоненциальном формате %.2e
 *   - Использует COLOR_PAIR(3) (зелёный текст) для выделения
 *   - Перед выводом очищает строки статуса пробелами
 *
 * Примечание:
 *   При изменении структуры AppState необходимо обновить формат вывода
 *   и количество очищаемых строк.
 * ================================================================================== */
void show_status_bar(AppState *state)
{
    int height, width;
    getmaxyx(stdscr, height, width);

    const char *m_str = (state->method_id == -1) ? "Не выбран" : methods[state->method_id];
    const char *m_str_square = (state->method_square == -1) ? "Не выбран" : methods_square[state->method_square];
    const char *f_str1 = (state->function1_id == -1) ? "Не выбрана" : functions1[state->function1_id];
    const char *f_str2 = (state->function2_id == -1) ? "Не выбрана" : functions2[state->function2_id];

    attron(COLOR_PAIR(3));

    // Очищаем 5 строк для статуса (добавили эпсилон)
    for (int i = 0; i < 6; i++)
    {
        mvhline(height - 6 + i, 0, ' ', width);
    }

    // Вывод статуса с эпсилоном (%.2e — экспоненциальный формат для малых чисел)
    mvprintw(height - 6, 0, " [Статус]   Метод расчета корней: %s", m_str);
    mvprintw(height - 5, 0, "            Метод расчета площади: %s", m_str_square);
    mvprintw(height - 4, 0, "            Функция1: %s", f_str1);
    mvprintw(height - 3, 0, "            Функция2: %s", f_str2);
    mvprintw(height - 2, 0, "            Интервал: [%.4f, %.4f]",
             state->start_interval, state->end_interval);
    mvprintw(height - 1, 0, "            Эпсилон: %.2e", state->epsylion);

    attroff(COLOR_PAIR(3));
    refresh();
}

