// F_3.c
// Цифры по возрастанию
// Написать функцию и программу демонстрирующую работу данной функции. 
// Вывести в порядке возрастания цифры, входящие в десятичную запись 
// натурального числа N, не более 1000 цифр. Цифра пробел сколько раз 
// данная цифра встречается в числе.
// Input format
//     Натуральное число не более 1000 цифр
// Output format
//     Цифры входящие в число через пробел в порядке возрастания. От самой 
// младшей цифры до самой старшей и количество.
// Examples
// Input
//     54321
// Output
// 1 1
// 2 1
// 3 1
// 4 1
// 5 1
// Input
//     22443344
// Output
// 2 2
// 3 2
// 4 4

#include <stdio.h>
#include <string.h>

void count_digits_and_print(const char *num_str) {
    int count[10] = {0};  // счётчики для '0'..'9'

    // Подсчёт частот
    for (int i = 0; num_str[i] != '\0'; ++i) {
        char c = num_str[i];
        if (c >= '0' && c <= '9') {
            count[c - '0']++;
        }
        // (по условию — натуральное число, так что других символов быть не должно)
    }

    // Вывод: цифра (по возрастанию) + количество, только если > 0
    for (int digit = 0; digit <= 9; ++digit) {
        if (count[digit] > 0) {
            printf("%d %d\n", digit, count[digit]);
        }
    }
}

int main() {
    char num_str[1005];  // до 1000 цифр + '\n' + '\0'

    // Читаем строку (без проблем с пробелами в начале/конце)
    if (scanf("%1004s", num_str) != 1) {
        return 1;  // ошибка ввода
    }

    count_digits_and_print(num_str);

    return 0;
}