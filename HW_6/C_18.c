// Количество цифр в тексте

// Составить логическую функцию, которая определяет, что текущий 
// символ это цифра. Написать программу считающую количество цифр 
// в тексте. int is_digit(char c)
// Input format
// Текст из английских букв и знаков препинания. В конце текста символ точка.
// Output format
// Одно целое число - количество цифр в тексте.

#include <stdio.h>

// Логическая функция: проверяет, является ли символ цифрой
int is_digit(char c) {
    return (c >= '0' && c <= '9');
}

int main() {
    char ch;
    int count = 0;

    while ((ch = getchar()) != '.') {
        if (is_digit(ch)) {
            count++;
        }
    }

    printf("%d\n", count);
    return 0;
}