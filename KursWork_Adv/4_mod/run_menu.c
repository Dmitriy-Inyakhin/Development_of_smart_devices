/* ==================================================================================
 * Функция: get_number_input
 * ==================================================================================
 * Назначение:
 *   Организует интерактивный ввод вещественного числа (double) в указанном
 *   окне ncurses с поддержкой редактирования и валидации формата.
 *
 * Параметры:
 *   win — указатель на окно ncurses, в котором производится ввод
 *   y   — координата Y (строка) для размещения поля ввода внутри окна
 *   x   — координата X (столбец) для размещения поля ввода внутри окна
 *
 * Возвращаемое значение:
 *   double — введённое пользователем число
 *   -1.0   — специальный код ошибки/отмены (пользователь нажал Esc)
 *
 * Особенности:
 *   - Поддерживаемые символы:
 *       • Цифры 0-9
 *       • Знак минуса '-' (только первый символ)
 *       • Десятичная точка '.' или запятая ',' (нормализуется к точке,
 *         допускается только одна)
 *       • Научная нотация 'e' или 'E' (например, 1.5e-3)
 *   - Клавиши управления:
 *       • Backspace — удаление последнего символа
 *       • Enter — подтверждение ввода
 *       • Esc — отмена ввода (возврат -1.0)
 *   - Максимальная длина ввода: MAX_INPUT_LEN - 1 символов
 *   - Временно переключает локаль на "C" для корректного парсинга
 *     десятичной точки функцией atof()/strtod()
 *
 * Примечание:
 *   Функция включает эхо (echo) и отображение курсора (curs_set(1))
 *   на время ввода, восстанавливая исходные настройки после завершения.
 * ================================================================================== */
double get_number_input(WINDOW *win, int y, int x)
{
    char buffer[MAX_INPUT_LEN] = {0};
    int pos = 0, ch;
    int has_dot = 0; // Флаг: уже введена точка

    noecho();
    curs_set(1);
    wmove(win, y, x);
    wrefresh(win);

    while (1)
    {
        ch = wgetch(win);
        if (ch == '\n' || ch == KEY_ENTER)
        {
            buffer[pos] = '\0';
            break;
        }
        else if (ch == KEY_BACKSPACE || ch == 127 || ch == 8)
        {
            if (pos > 0)
            {
                // Если удаляем точку, сбрасываем флаг
                if (buffer[pos - 1] == '.' || buffer[pos - 1] == ',')
                {
                    has_dot = 0;
                }
                pos--;
                buffer[pos] = '\0';
                wmove(win, y, x);
                wclrtoeol(win);
                wprintw(win, "%s", buffer);
                wmove(win, y, x + pos);
                wrefresh(win);
            }
        }
        else if ((ch >= '0' && ch <= '9') && pos < MAX_INPUT_LEN - 1)
        {
            buffer[pos++] = ch;
            waddch(win, ch);
            wrefresh(win);
        }
        else if ((ch == '.' || ch == ',') && !has_dot && pos < MAX_INPUT_LEN - 1)
        {
            // Разрешаем только одну точку/запятую
            buffer[pos++] = '.'; // Нормализуем запятую к точке
            has_dot = 1;
            waddch(win, '.');
            wrefresh(win);
        }
        else if ((ch == '-' || ch == '_') && pos == 0 && pos < MAX_INPUT_LEN - 1)
        {
            // Разрешаем минус только в самом начале строки (первый символ)
            buffer[pos++] = '-';
            waddch(win, '-');
            wrefresh(win);
        }
        else if (ch == 27)
        { // Esc - отмена
            noecho();
            curs_set(0);
            return -1.0; // Специальное значение ошибки
        }
    }
    noecho();
    curs_set(0);

    // if (pos > 0) {
    //     return atof(buffer);  // Преобразование строки в double
    // }
    // return -1.0;

    if (pos > 0)
    {
        // === КЛЮЧЕВОЕ ИСПРАВЛЕНИЕ ===
        // Сохраняем текущую локаль
        char *old_locale = setlocale(LC_NUMERIC, NULL);
        char *saved_locale = NULL;
        if (old_locale)
        {
            saved_locale = strdup(old_locale); // Требуется #include <string.h>
        }

        // Временно ставим "C" локаль для корректного парсинга точки
        setlocale(LC_NUMERIC, "C");
        double result = atof(buffer);

        // Восстанавливаем исходную локаль
        if (saved_locale)
        {
            setlocale(LC_NUMERIC, saved_locale);
            free(saved_locale);
        }
        // ==============================

        return result;
    }
    return -1.0;
}

/* ==================================================================================
 * Функция: run_epsilon_submenu
 * ==================================================================================
 * Назначение:
 *   Запускает подменю для ввода и валидации значения точности вычислений
 *   (эпсилон), используемого в численных методах как критерий остановки.
 *
 * Параметры:
 *   state — указатель на структуру AppState для сохранения результата
 *
 * Возвращаемое значение:
 *   Нет (void)
 *
 * Особенности:
 *   - Создаёт модальное окно 50x10 символов по центру экрана
 *   - Требует ввода положительного числа в диапазоне (0.0, 1.0)
 *   - Валидация:
 *       • eps > 0.0 — точность должна быть положительной
 *       • eps < 1.0 — типичное ограничение для итерационных методов
 *   - При успешном вводе значение сохраняется в state->epsylion
 *   - При ошибке выводится сообщение красного цвета (COLOR_PAIR(4))
 *   - Поддерживает отмену через Esc (значение не изменяется)
 *
 * Примечание:
 *   Диапазон допустимых значений может быть изменён в зависимости от
 *   требований конкретных численных методов.
 * ================================================================================== */
void run_epsilon_submenu(AppState *state)
{
    int h, w;
    getmaxyx(stdscr, h, w);

    // Высота окна: 10 строк (заголовок, поле ввода, подсказки, рамка)
    int win_h = 10;
    int win_w = 50;
    WINDOW *win = newwin(win_h, win_w, (h - win_h) / 2, (w - win_w) / 2);

    keypad(win, TRUE); // Включаем обработку спецклавиш

    box(win, 0, 0);
    mvwprintw(win, 0, (win_w - 20) / 2, " ВВОД ЗНАЧЕНИЯ EPSILON ");
    mvwprintw(win, 2, 2, "Введите точность (eps):");
    mvwprintw(win, 4, 2, "Пример: 0.001, 1e-5, 0.01");
    mvwprintw(win, 7, 2, "Esc - отмена, Enter - ввод");
    wrefresh(win);
    noecho();

    double eps = get_number_input(win, 3, 2);

    // Валидация: эпсилон должен быть положительным и маленьким
    if (eps > 0.0 && eps < 1.0)
    {
        state->epsylion = eps;
        mvwprintw(win, 7, 2, "Успешно! Нажмите клавишу...    ");
    }
    else if (eps >= 1.0)
    {
        attron(COLOR_PAIR(4));
        mvwprintw(win, 7, 2, "Ошибка: eps должен быть < 1.0   ");
        attroff(COLOR_PAIR(4));
    }
    else if (eps <= 0.0 && eps != -1.0)
    {
        attron(COLOR_PAIR(4));
        mvwprintw(win, 7, 2, "Ошибка: eps должен быть > 0     ");
        attroff(COLOR_PAIR(4));
    }
    // Если eps == -1.0 — пользователь нажал Esc, ничего не делаем

    wrefresh(win);
    wgetch(win); // Ждём нажатия для возврата
    erase();
    delwin(win);
}

/* ==================================================================================
 * Функция: run_interval_submenu
 * ==================================================================================
 * Назначение:
 *   Запускает подменю для ввода границ расчётного интервала [start, end],
 *   в котором будут выполняться численные расчёты.
 *
 * Параметры:
 *   state — указатель на структуру AppState для сохранения результата
 *
 * Возвращаемое значение:
 *   Нет (void)
 *
 * Особенности:
 *   - Создаёт модальное окно 50x10 символов по центру экрана
 *   - Последовательно запрашивает начало и конец интервала
 *   - Поддерживает ввод:
 *       • Отрицательных чисел (например, -10.5)
 *       • Положительных чисел (например, 5.0)
 *       • Вещественных чисел с десятичной точкой
 *   - Валидация:
 *       • end >= start — конец интервала не может быть меньше начала
 *   - При успешном вводе значения сохраняются в state->start_interval
 *     и state->end_interval
 *   - При ошибке выводится сообщение красного цвета (COLOR_PAIR(4))
 *   - Поддерживает отмену через Esc (значения не изменяются)
 *
 * Примечание:
 *   Для некоторых методов (например, метод половинного деления) может
 *   потребоваться дополнительная проверка знака функции на границах.
 * ================================================================================== */
void run_interval_submenu(AppState *state)
{
    int h, w;
    getmaxyx(stdscr, h, w);

    int win_h = 10;
    int win_w = 50;
    WINDOW *win = newwin(win_h, win_w, (h - win_h) / 2, (w - win_w) / 2);

    keypad(win, TRUE);

    box(win, 0, 0);
    mvwprintw(win, 0, (win_w - 18) / 2, " ВВОД ИНТЕРВАЛА ");

    // Обновленные подсказки: явно указываем, что отрицательные числа разрешены
    mvwprintw(win, 2, 2, "Начало интервала:");
    mvwprintw(win, 4, 2, "Конец интервала:");
    mvwprintw(win, 6, 2, "(допустимы отрицат. числа)");
    mvwprintw(win, 7, 2, "Esc - отмена, Enter - ввод");
    wrefresh(win);
    noecho();

    double start = get_number_input(win, 3, 2);

    // Проверка: если не отмена (-1.0), принимаем любое число
    if (start != -1.0)
    {
        double end = get_number_input(win, 5, 2);

        // Главное условие: конец должен быть больше или равен началу
        if (end != -1.0 && end >= start)
        {
            state->start_interval = start;
            state->end_interval = end;
            mvwprintw(win, 7, 2, "Успешно! Нажмите клавишу...   ");
        }
        else if (end != -1.0)
        {
            // Ошибка логики: например, start=10, end=5
            attron(COLOR_PAIR(4));
            mvwprintw(win, 7, 2, "Ошибка: Конец < Начала        ");
            attroff(COLOR_PAIR(4));
        }
        // Если end == -1.0, пользователь нажал Esc, просто выходим
    }

    wrefresh(win);
    wgetch(win);
    erase();
    delwin(win);
}

/* ==================================================================================
 * Функция: run_simple_list_submenu
 * ==================================================================================
 * Назначение:
 *   Универсальная функция для отображения подменю-списка с возможностью
 *   выбора одного пункта из предложенных вариантов. Используется для
 *   выбора метода расчёта и функций.
 *
 * Параметры:
 *   state — указатель на структуру AppState для сохранения результата
 *   title — заголовок окна подменю (отображается в верхней рамке)
 *   items — массив строк (const char**) с текстами пунктов меню
 *   count — количество пунктов в массиве items
 *   type  — идентификатор типа выбора для определения поля сохранения:
 *           1 — state->method_id (выбор метода расчёта)
 *           2 — state->function1_id (выбор первой функции)
 *           3 — state->function2_id (выбор второй функции)
 *
 * Возвращаемое значение:
 *   Нет (void)
 *
 * Особенности:
 *   - Высота окна рассчитывается автоматически: count + 5 строк
 *     (пункты + заголовок + рамка + подсказка + пункт "Выход")
 *   - Ширина окна: 45 символов
 *   - Навигация:
 *       • KEY_UP / KEY_DOWN — перемещение по пунктам
 *       • Enter — подтверждение выбора
 *       • Esc или пункт "0. [ВЫХОД]" — выход без сохранения
 *   - Текущий пункт подсвечивается инверсией цветов (COLOR_PAIR(1))
 *   - Автоматически добавляет пункт выхода в конец списка
 *   - Циклическая навигация (переход с последнего на первый и наоборот)
 *
 * Примечание:
 *   Функция является универсальной и может использоваться для любых
 *   списков выбора без изменения кода — достаточно передать массив
 *   строк и правильный type.
 * ================================================================================== */
void run_simple_list_submenu(AppState *state, const char *title, const char **items, int count, int type)
{
    int h, w;
    getmaxyx(stdscr, h, w);

    // +1 для пункта "Выход", +4 для рамки и заголовка
    int win_h = count + 1 + 4;
    int win_w = 45;
    WINDOW *win = newwin(win_h, win_w, (h - win_h) / 2, (w - win_w) / 2);

    // Ключевое исправление: включаем обработку спецклавиш для этого окна
    keypad(win, TRUE);

    int current = 0; // Индекс выделенного элемента (0..count)
    int ch;
    int total_items = count + 1; // общее число пунктов с учетом "Выход"

    while (1)
    {
        // Полная перерисовка окна
        box(win, 0, 0);
        mvwprintw(win, 0, (win_w - strlen(title)) / 2, " %s ", title);
        mvwprintw(win, win_h - 2, 2, "Esc/0 - выход, Enter - выбор");

        // Рисуем основные пункты
        for (int i = 0; i < count; i++)
        {
            if (i == current)
            {
                wattron(win, COLOR_PAIR(1));
                mvwprintw(win, i + 2, 2, "%s", items[i]);
                wattroff(win, COLOR_PAIR(1));
            }
            else
            {
                mvwprintw(win, i + 2, 2, "%s", items[i]);
            }
        }

        // Рисуем пункт "Выход" отдельно (всегда последний)
        int exit_idx = count;
        if (exit_idx == current)
        {
            wattron(win, COLOR_PAIR(1));
            mvwprintw(win, exit_idx + 2, 2, "0. [ВЫХОД в главное меню]");
            wattroff(win, COLOR_PAIR(1));
        }
        else
        {
            mvwprintw(win, exit_idx + 2, 2, "0. [ВЫХОД в главное меню]");
        }

        wrefresh(win);

        ch = wgetch(win);

        if (ch == KEY_UP)
        {
            current = (current - 1 + total_items) % total_items;
        }
        else if (ch == KEY_DOWN)
        {
            current = (current + 1) % total_items;
        }
        else if (ch == '\n' || ch == KEY_ENTER || ch == '0')
        {
            // Если выбран пункт "Выход" (индекс count) или нажата '0'
            if (current == count || ch == '0')
            {
                break; // Просто выход без сохранения
            }
            // Сохраняем выбор
            if (type == 1)
                state->method_id = current;
            if (type == 2)
                state->function1_id = current;
            if (type == 3)
                state->function2_id = current;
            if (type == 4)
                state->method_square = current;
            break;
        }
        else if (ch == 27)
        { // Esc
            break;
        }
    }
    erase();
    delwin(win);
}

/* ==================================================================================
 * Функция: show_result
 * ==================================================================================
 * Назначение:
 *   Выводит модальное окно с результатами выполненных расчетов.
 *
 * Параметры:
 *   double result - значение полученное в результате расчнта
 *
 * Возвращаемое значение:
 *   Нет (void)
 *
 * Особенности:
 *   - Окно центрируется на экране (размер 50x12 символов)
 *   - Блокирует работу программы до нажатия любой клавиши (wgetch)
 *   - После закрытия окна экран очищается (erase) для последующей
 *     перерисовки главного меню
 *
 * Примечание:
 *   
 * ================================================================================== */
void show_result(double result)
{
    int h, w;
    getmaxyx(stdscr, h, w);
    WINDOW *win = newwin(7, 50, (h - 7) / 2, (w - 50) / 2);

    keypad(win, TRUE); // Включаем спецклавиши для окна
    box(win, 0, 0);
    mvwprintw(win, 0, (50 - 10) / 2, " РЕЗУЛЬТАТ ");
    mvwprintw(win, 2, 2, "Результат расчета: %.4f", result);
    mvwprintw(win, 4, 2, "Нажмите любую клавишу...");
    wrefresh(win);
    wgetch(win); // Ждем нажатия для возврата
    erase();
    delwin(win);
}
