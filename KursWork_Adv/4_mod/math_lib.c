/* ==================================================================================
 * Функция: f_diff
 * ==================================================================================
 * Назначение:
 *   Вычисляет разность значений двух функций в заданной точке: f1(x) - f2(x).
 *   Используется для поиска точки пересечения графиков: корень уравнения
 *   f_diff(f1, f2, x) = 0 соответствует решению f1(x) = f2(x).
 *
 * Параметры:
 *   f1 — указатель на первую функцию типа double (*)(double)
 *   f2 — указатель на вторую функцию типа double (*)(double)
 *   x  — аргумент, в котором вычисляется разность
 *
 * Возвращаемое значение:
 *   double — результат вычисления f1(x) - f2(x)
 *
 * Особенности:
 *   - Универсальная функция: работает с любыми двумя функциями сигнатуры double(double)
 *   - При поиске пересечения f1 и f3 вызывать как: f_diff(f1, f3, x)
 *   - Порядок аргументов важен: f_diff(a, b, x) = -f_diff(b, a, x)
 *
 * Пример использования:
 *   double (*funcA)(double) = f2;
 *   double (*funcB)(double) = f3;
 *   double result = f_diff(funcA, funcB, 2.5);  // Вычислит f2(2.5) - f3(2.5)
 * ================================================================================== */
double f_diff(function f_1, function f_2, double x)
{
    /* Проверка на NULL-указатели для безопасности */
    if (f_1 == NULL || f_2 == NULL)
    {
        fprintf(stderr, "[f_diff] Error: NULL function pointer\n");
        return 0.0;
    }

    return f_1(x) - f_2(x);
}

/* ==================================================================================
 * Функция: signFunc
 * ==================================================================================
 * Назначение:
 *   Определяет знак значения функции в заданной точке.
 *
 * Параметры:
 *   x — точка вычисления
 *   f — указатель на функцию
 *
 * Возвращаемое значение:
 *   int — 1 если f(x)>0, -1 если f(x)<0, 0 если f(x)==0
 *
 * Особенности:
 *   - Используется в методах, требующих проверки смены знака (бисекция, хорд)
 * ================================================================================== */
int signFunc(double x, function f)
{
    double val = f(x);
    if (fabs(val) < 1e-12)
        return 0; /* Считаем близким к нулю */
    return (val < 0.0) ? -1 : 1;
}

/* ==================================================================================
 * Функция: rootFindLineSearch
 * ==================================================================================
 * Назначение:
 *   Реализует метод линейного поиска корня функции на отрезке [xl, xr].
 *
 * Параметры:
 *   xl, xr — границы интервала поиска
 *   eps    — точность по значению функции |f(x)| < eps
 *   f      — указатель на целевую функцию
 *
 * Возвращаемое значение:
 *   double — приближённое значение корня
 *
 * Особенности:
 *   - Простой перебор с шагом eps
 *   - Может быть медленным при малых eps
 *   - Возвращает последнее значение x, если точный корень не найден
 * ================================================================================== */
double rootFindLineSearch(double xl, double xr, double eps, function f_1, function f_2)
{
    double x = xl;
    double step = eps;
    int stepcount = 0;
    const int MAX_STEPS = 1000000; /* Защита от бесконечного цикла */

    while (x <= xr && stepcount < MAX_STEPS)
    {
        stepcount++;
        if (fabs( f_diff(f_1, f_2, x) ) < eps)
        {
            //printf("[LineSearch] Корень найден: x=%.6f, f(x)=%.2e за %d шагов\n",
              //     x, f_diff(f_1, f_2, x), stepcount);
            return x;
        }
        x += step;
    }

    //printf("[LineSearch] Точный корень не найден за %d шагов, возврат x=%.6f\n",
    //       stepcount, x);
    return x;
}

/* ==================================================================================
 * Функция: rootFindDiv2
 * ==================================================================================
 * Назначение:
 *   Реализует метод деления отрезка пополам (бисекции) для поиска корня.
 *
 * Параметры:
 *   xl, xr — границы интервала, где f(xl)*f(xr) < 0
 *   eps    — точность по длине отрезка |xr-xl| < eps
 *   f      — указатель на целевую функцию
 *
 * Возвращаемое значение:
 *   double — приближённое значение корня (середина последнего отрезка)
 *
 * Особенности:
 *   - Гарантированная сходимость при выполнении условия смены знака
 *   - Линейная скорость сходимости
 *   - Проверка границ на точное совпадение с корнем
 * ================================================================================== */
double rootFindDiv2(double xl, double xr, double eps, function f_1, function f_2)
{
    int stepcount = 0;
    double xm;
    const int MAX_ITER = 1000;

    /* Проверка границ */
    if (fabs(f_diff(f_1, f_2, xl)) < eps)
    {
       // printf("[Bisect] Корень на левой границе: x=%.6f\n", xl);
        return xl;
    }
    if (fabs(f_diff(f_1, f_2, xr)) < eps)
    {
        //printf("[Bisect] Корень на правой границе: x=%.6f\n", xr);
        return xr;
    }

    /* Проверка условия сходимости */
    if (f_diff(f_1, f_2, xl) * f_diff(f_1, f_2, xr) > 0)
    {
        //fprintf(stderr, "[Bisect] Warning: f(xl)*f(xr) > 0, корень может отсутствовать\n");
    }

    while ((fabs(xr - xl) > eps) && (stepcount < MAX_ITER))
    {
        stepcount++;
        xm = (xl + xr) / 2.0;

        if (fabs(f_diff(f_1, f_2, xm)) < eps)
        {
           // printf("[Bisect] Корень найден: x=%.6f за %d шагов\n", xm, stepcount);
            return xm;
        }

        /* Сужение интервала */
        if (f_diff(f_1, f_2, xl) * f_diff(f_1, f_2, xm) < 0.0)
            xr = xm;
        else
            xl = xm;
    }

   // printf("[Bisect] Завершено за %d шагов, точность: |xr-xl|=%.2e\n",
        //   stepcount, fabs(xr - xl));
    return (xl + xr) / 2.0;
}

/* ==================================================================================
 * Функция: rootFindChord
 * ==================================================================================
 * Назначение:
 *   Реализует метод хорд (секущих) для поиска корня функции.
 *
 * Параметры:
 *   xl, xr — границы интервала
 *   eps    — точность по изменению аргумента |x_new - x_old| < eps
 *   f      — указатель на целевую функцию
 *
 * Возвращаемое значение:
 *   double — приближённое значение корня
 *
 * Особенности:
 *   - Использует линейную интерполяцию между точками
 *   - Быстрее бисекции, но требует осторожности при выборе начальных точек
 *   - Защита от деления на ноль и бесконечных итераций
 * ================================================================================== */
double rootFindChord(double xl, double xr, double eps, function f_1, function f_2)
{
    int stepcount = 0;
    double x_prev = xl;
    double x_curr = xr;
    const int MAX_ITER = 1000;
    double f_xl = f_diff(f_1, f_2, xl); /* Фиксируем f(xl) для формулы */

    /* Проверка границ */
    if (fabs(f_xl) < eps)
        return xl;
    if (fabs(f_diff(f_1, f_2, xr)) < eps)
        return xr;

    while (fabs(x_curr - x_prev) > eps && stepcount < MAX_ITER)
    {
        stepcount++;
        x_prev = x_curr;
        double f_prev = f_diff(f_1, f_2, x_prev);

        /* Формула хорды: защита от деления на ноль */
        double denom = f_prev - f_xl;
        if (fabs(denom) < 1e-12)
        {
            //fprintf(stderr, "[Chord] Деление на ноль на шаге %d\n", stepcount);
            break;
        }

        x_curr = x_prev - f_prev * (x_prev - xl) / denom;

        /* Проверка выхода за границы интервала */
        if (x_curr < xl || x_curr > xr)
        {
           // fprintf(stderr, "[Chord] Выход за границы интервала, остановка\n");
            break;
        }
    }

   // printf("[Chord] Завершено за %d шагов, x=%.6f, f(x)=%.2e\n",
     //      stepcount, x_curr, f_diff(f_1, f_2, x_curr));
    return x_curr;
}

/* ==================================================================================
 * Функция: rootFindTangent
 * ==================================================================================
 * Назначение:
 *   Реализует метод Ньютона (касательных) для поиска корня функции.
 *
 * Параметры:
 *   xn  — начальное приближение
 *   eps — точность по изменению аргумента |x_new - x_old| < eps
 *   f   — указатель на целевую функцию
 *   df  — указатель на производную функции
 *
 * Возвращаемое значение:
 *   double — приближённое значение корня
 *
 * Особенности:
 *   - Квадратичная скорость сходимости вблизи корня
 *   - Требует вычисления производной
 *   - Может расходиться при плохом начальном приближении
 * ================================================================================== */
double rootFindTangent(double xn, double eps, function f, function df)
{
    double x_curr = xn;
    double x_prev;
    int stepcount = 0;
    const int MAX_ITER = 100;

    do
    {
        x_prev = x_curr;
        double f_val = f(x_prev);
        double df_val = df(x_prev);

        if (fabs(df_val) < 1e-12)
        {
           // fprintf(stderr, "[Newton] Производная близка к нулю, остановка\n");
            break;
        }

        x_curr = x_prev - f_val / df_val;
        stepcount++;

    } while (fabs(x_curr - x_prev) > eps && stepcount < MAX_ITER);

    //printf("[Newton] Завершено за %d шагов, x=%.6f, f(x)=%.2e\n",
      //     stepcount, x_curr, f(x_curr));
    return x_curr;
}

