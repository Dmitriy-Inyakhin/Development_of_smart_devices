/* ==================================================================================
 * Функция: calcIntegralSquare
 * ==================================================================================
 * Назначение:
 *   Вычисляет определённый интеграл методом прямоугольников (левые).
 *
 * Параметры:
 *   xl, xr — границы интегрирования
 *   n      — количество разбиений отрезка
 *   f      — указатель на подынтегральную функцию
 *
 * Возвращаемое значение:
 *   double — приближённое значение интеграла
 *
 * Особенности:
 *   - Формула: h * Σ f(x_i), где x_i = xl + i*h
 *   - Погрешность O(h) — линейная
 *   - При увеличении n точность растёт
 * ================================================================================== */
double calcIntegralSquare(double xl, double xr, size_t n, function f)
{
    if (n == 0)
        return 0.0;

    double sum = 0.0;
    double h = (xr - xl) / (double)n;

    for (size_t i = 0; i < n; i++)
    {
        double x = xl + i * h;
        sum += f(x);
    }
    return sum * h;
}

/* ==================================================================================
 * Функция: calcIntegralTrap
 * ==================================================================================
 * Назначение:
 *   Вычисляет определённый интеграл методом трапеций.
 *
 * Параметры:
 *   xl, xr — границы интегрирования
 *   n      — количество разбиений отрезка
 *   f      — указатель на подынтегральную функцию
 *
 * Возвращаемое значение:
 *   double — приближённое значение интеграла
 *
 * Особенности:
 *   - Формула: h * (f(x0)/2 + f(x1) + ... + f(xn)/2)
 *   - Погрешность O(h²) — квадратичная
 *   - Точнее метода прямоугольников при том же n
 * ================================================================================== */
double calcIntegralTrap(double xl, double xr, size_t n, function f)
{
    if (n == 0)
        return 0.0;

    double sum = 0.0;
    double h = (xr - xl) / (double)n;

    /* Крайние точки с коэффициентом 1/2 */
    sum += f(xl) / 2.0 + f(xr) / 2.0;

    /* Внутренние точки с коэффициентом 1 */
    for (size_t i = 1; i < n; i++)
    {
        double x = xl + i * h;
        sum += f(x);
    }
    return sum * h;
}

/* ==================================================================================
 * Функция: calcIntegralSimpson
 * ==================================================================================
 * Назначение:
 *   Вычисляет определённый интеграл методом Симпсона (парабол).
 *
 * Параметры:
 *   xl, xr — границы интегрирования
 *   n      — количество разбиений (автоматически округляется до чётного)
 *   f      — указатель на подынтегральную функцию
 *
 * Возвращаемое значение:
 *   double — приближённое значение интеграла
 *
 * Особенности:
 *   - Формула: h/3 * (f0 + 4f1 + 2f2 + 4f3 + ... + fn)
 *   - Погрешность O(h⁴) — высокая точность
 *   - Требует чётного числа отрезков (n автоматически корректируется)
 * ================================================================================== */
double calcIntegralSimpson(double xl, double xr, size_t n, function f)
{
    if (n == 0)
        return 0.0;

    /* Метод Симпсона требует чётного n */
    if (n % 2 != 0)
        n++;

    double sum = 0.0;
    double h = (xr - xl) / (double)n;

    /* Крайние точки */
    sum += f(xl) + f(xr);

    /* Внутренние точки с чередующимися коэффициентами 4 и 2 */
    for (size_t i = 1; i < n; i++)
    {
        double x = xl + i * h;
        sum += (i % 2 == 0) ? 2.0 * f(x) : 4.0 * f(x);
    }
    return sum * h / 3.0;
}

/* ==================================================================================
 * Функция: calcIntegralMonteCarlo
 * ==================================================================================
 * Назначение:
 *   Вычисляет определённый интеграл методом Монте-Карло.
 *
 * Параметры:
 *   xl, xr  — границы интегрирования
 *   fmax    — оценка максимума |f(x)| на отрезке (для ограничения по Y)
 *   n       — количество случайных испытаний
 *   f       — указатель на подынтегральную функцию
 *
 * Возвращаемое значение:
 *   double — приближённое значение интеграла
 *
 * Особенности:
 *   - Статистический метод, точность ~ 1/√n
 *   - Не требует гладкости функции
 *   - fmax должно быть >= max|f(x)| на [xl, xr]
 *   - Требует инициализации генератора случайных чисел (srand)
 * ================================================================================== */
double calcIntegralMonteCarlo(double xl, double xr, double fmax, size_t n, function f)
{
    if (n == 0 || fmax <= 0.0)
        return 0.0;

    size_t count_under = 0;
    double width = fabs(xr - xl);

    /* Инициализация ГСЧ при первом вызове */
    static int seeded = 0;
    if (!seeded)
    {
        srand((unsigned)time(NULL));
        seeded = 1;
    }

    for (size_t i = 0; i < n; i++)
    {
        /* Равномерное распределение в [xl, xr] */
        double x = xl + ((double)rand() / RAND_MAX) * width;

        /* Равномерное распределение в [0, fmax] с учётом знака */
        double y_limit = (f(x) >= 0) ? fmax : -fmax;
        double y = ((double)rand() / RAND_MAX) * y_limit;

        /* Проверка попадания точки под график */
        if (f(x) >= 0)
        {
            if (y >= 0 && y <= f(x))
                count_under++;
        }
        else
        {
            if (y <= 0 && y >= f(x))
                count_under++;
        }
    }

    /* Площадь = (доля попаданий) * (площадь ограничивающего прямоугольника) */
    double ratio = (double)count_under / (double)n;
    return ratio * width * fabs(fmax);
}

/* ==================================================================================
 * Функция: find_root_by_state
 * ==================================================================================
 * Назначение:
 *   Универсальная обёртка для поиска корня с параметрами из AppState.
 *   Автоматически выбирает метод расчёта на основе state->method_id.
 *
 * Параметры:
 *   state — указатель на структуру с настройками
 *   f     — указатель на целевую функцию
 *
 * Возвращаемое значение:
 *   double — найденное приближение корня
 *
 * Особенности:
 *   - method_id: 0=линейный поиск, 1=бисекция, 2=хорд, 3=Ньютон
 *   - Для метода Ньютона автоматически подбирается производная
 *   - Использует state->start_interval, state->end_interval, state->epsylion
 * ================================================================================== */
double find_root_by_state(AppState *state)
{
    if (!state)
    {
        fprintf(stderr, "[find_root_by_state] Error: NULL pointer\n");
        return 0.0;
    }

    double xl = state->start_interval;
    double xr = state->end_interval;
    double eps = state->epsylion;
    function f_1 = get_function_by_id(state->function1_id);
    function f_2 = get_function_by_id(state->function2_id);

    //printf("\n[ROOT FIND] Интервал: [%.4f, %.4f], eps: %.2e, метод: %d\n",
    //       xl, xr, eps, state->method_id);

    switch (state->method_id)
    {
    case 0: /* Линейный поиск */
        return rootFindLineSearch(xl, xr, eps, f_1, f_2);

    case 1: /* Деление пополам */
        return rootFindDiv2(xl, xr, eps, f_1, f_2);

    case 2: /* Метод хорд */
        return rootFindChord(xl, xr, eps, f_1, f_2);

    case 3: /* Метод Ньютона (требует производную) */
    {
        /* Начальное приближение — середина интервала */
        double x0 = (xl + xr) / 2.0;
        /* Здесь нужно передать соответствующую производную */
        /* Для упрощения используем df1 как заглушку */
        return rootFindTangent(x0, eps, f_1, df1);
    }

    default:
       // fprintf(stderr, "[find_root_by_state] Unknown method_id: %d, using bisection\n",
         //       state->method_id);
        return rootFindDiv2(xl, xr, eps, f_1, f_2);
    }
}

/* ==================================================================================
 * Функция: calc_area_by_state
 * ==================================================================================
 * Назначение:
 *   Универсальная обёртка для вычисления интеграла с параметрами из AppState.
 *   Автоматически выбирает метод интегрирования на основе state->method_square.
 *
 * Параметры:
 *   state   — указатель на структуру с настройками
 *   f       — указатель на подынтегральную функцию
 *   n_steps — количество разбиений для детерминированных методов
 *
 * Возвращаемое значение:
 *   double — приближённое значение определённого интеграла
 *
 * Особенности:
 *   - method_square: 0=прямоугольники, 1=трапеции, 2=Симпсон, 3=Монте-Карло
 *   - Для Монте-Карло n_steps интерпретируется как число испытаний
 *   - Для Монте-Карло требуется оценка fmax (берётся как 10*max|f| на границах)
 * ================================================================================== */
double calc_area_by_state(AppState *state)
{
    if (!state)
    {
        //fprintf(stderr, "[calc_area_by_state] Error: NULL pointer\n");
        return 0.0;
    }

    double xl = state->start_interval;
    double xr = state->end_interval;
    double n_steps = (xr - xl) / state->epsylion;
    function f = get_function_by_id(state->function1_id);

    // printf("\n[INTEGRAL] Интервал: [%.4f, %.4f], шаги: %zu, метод: %d\n",
    //        xl, xr, n_steps, state->method_square);

    switch (state->method_square)
    {
    case 0: /* Прямоугольники */
        return fabs(calcIntegralSquare(xl, xr, n_steps, f));

    case 1: /* Трапеции */
        return fabs(calcIntegralTrap(xl, xr, n_steps, f));

    case 2: /* Симпсон */
        return fabs(calcIntegralSimpson(xl, xr, n_steps, f));

    case 3: /* Монте-Карло */
    {
        /* Оценка fmax: максимум из |f(xl)|, |f(xr)| с запасом */
        double f_max = fmax(fabs(f(xl)), fabs(f(xr))) * 2.0;
        if (f_max < 1.0)
            f_max = 1.0; /* Минимальное значение */
        return fabs(calcIntegralMonteCarlo(xl, xr, f_max, n_steps, f));
    }

    default:
        // fprintf(stderr, "[calc_area_by_state] Unknown method_square: %d, using trapezoids\n",
        //         state->method_square);
        return fabs(calcIntegralTrap(xl, xr, n_steps, f));
    }
}

