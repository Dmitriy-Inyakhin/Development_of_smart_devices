// F_16.c
// Клетки
// Известно, что шахматная доска имеет размерность 8х8 и состоит из клеток 2х 
// цветов, например, черного и белого (см. рисунок). Каждая клетка имеет 
// координату, состоящую из буквы и цифры. Горизонтальное расположение клетки 
// определяется буквой от A до H, а вертикальное – цифрой от 1 до 8. Заметим, 
// что клетка с координатой А1 имеет черный цвет. Требуется по заданной 
// координате определить цвет клетки.

// Формат входных данных
// В единственной строке входного файла записана координата клетки на шахматной 
// доске: всего два символа – буква и цифра (без пробелов).
// Формат результата
// В выходной файл нужно вывести «WHITE», если указанная клетка имеет белый цвет 
// и «BLACK», если она черная.
// Примеры
// Входные данные
// C3
// Результат работы
// BLACK
// Входные данные
// G8
// Результат работы
// WHITE

#include <stdio.h>
#include <ctype.h>

int main() {
    char coord[3];
    
    if (scanf("%2s", coord) != 1) {
        fprintf(stderr, "Input error\n");
        return 1;
    }
    
    char letter = toupper(coord[0]);
    char digit_char = coord[1];
    
    if (letter < 'A' || letter > 'H' || digit_char < '1' || digit_char > '8') {
        fprintf(stderr, "Invalid coordinate\n");
        return 1;
    }
    
    int col = letter - 'A';  // A=0, B=1, ..., H=7
    int row = digit_char - '1';  // '1'=0, '2'=1, ..., '8'=7
    
    // Клетка A1 (col=0, row=0) — черная
    // Цвет чередуется: (col + row) % 2 == 0 → BLACK, иначе WHITE
    if ((col + row) % 2 == 0) {
        printf("BLACK\n");
    } else {
        printf("WHITE\n");
    }
    
    return 0;
}