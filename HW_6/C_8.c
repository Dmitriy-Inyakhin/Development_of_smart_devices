// Большими буквами

// Составить функцию, которая переводит латинскую строчную букву в заглавную.
// И показать пример ее использования.

// Input format
// Строка состоящая из английских букв, пробелов и запятых. В конце строки
// символ точка.
// Output format
// Исходная строка в которой маленькие английские буквы заменены заглавными.

#include <stdio.h>

// Функция для преобразования одной строчной латинской буквы в заглавную
char to_upper(char c)
{
    if (c >= 'a' && c <= 'z')
    {
        return c - 'a' + 'A';
    }
    return c; // если не строчная буква — возвращаем как есть
}

int main()
{
    char ch;
    while ((ch = getchar()) != '.')
    {
        putchar(to_upper(ch));
        if (ch == '.')
        {
            break; // точка — конец строки
        }
    }
    printf("\n");
    return 0;
}