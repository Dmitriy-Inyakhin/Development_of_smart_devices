/* ==================================================================================
 * Функция: f1, f2, f3
 * ==================================================================================
 * Назначение:
 *   Реализация целевых функций для численных расчётов.
 *
 * Параметры:
 *   x — аргумент функции
 *
 * Возвращаемое значение:
 *   double — значение функции в точке x
 *
 * Формулы:
 *   f1(x) = 0.6*x + 3
 *   f2(x) = (x-2)^3 - 1
 *   f3(x) = 3 / x  (опасность деления на 0 при x=0!)
 * ================================================================================== */
double f1(double x)
{
    return 0.6 * x + 3.0;
}

double f2(double x)
{
    return pow(x - 2.0, 3.0) - 1.0;
}

double f3(double x)
{
    /* Защита от деления на ноль */
    if (fabs(x) < 1e-10)
    {
        return (x >= 0) ? 1e10 : -1e10;
    }
    return 3.0 / x;
}

/* ==================================================================================
 * Функция: f_zero
 * ==================================================================================
 * Назначение:
 *   Функция-заглушка, которая всегда возвращает 0.0.
 *   Используется как безопасная альтернатива NULL для режима
 *   "функции с нулем" (function_id = 3).
 *
 * Параметры:
 *   x — аргумент функции (игнорируется)
 *
 * Возвращаемое значение:
 *   double — всегда 0.0
 *
 * Особенности:
 *   - Совместима с типом function (double (*)(double))
 *   - Не выполняет вычислений, работает за O(1)
 *   - Безопасна для использования в численных методах
 *   - Подавляет предупреждение о неиспользуемом параметре через (void)x
 *
 * Пример использования:
 *   function f = get_function_by_id(3);  // Вернёт f_zero
 *   double result = f(5.0);              // result = 0.0
 * ================================================================================== */
double f_zero(double x)
{
    (void)x; /* Явно помечаем параметр как неиспользуемый */
    return 0.0;
}

/* ==================================================================================
 * Функция: df1, df2, df3
 * ==================================================================================
 * Назначение:
 *   Реализация аналитических производных целевых функций.
 *   Используются в методе Ньютона (касательных).
 *
 * Параметры:
 *   x — аргумент производной
 *
 * Возвращаемое значение:
 *   double — значение производной в точке x
 *
 * Формулы:
 *   f1'(x) = 0.6
 *   f2'(x) = 3*(x-2)^2
 *   f3'(x) = -3/x^2
 * ================================================================================== */

double df1(double x)
{
    (void)x; /* Подавление предупреждения о неиспользуемом параметре */
    return 0.6;
}

double df2(double x)
{
    return 3.0 * pow(x - 2.0, 2.0);
}

double df3(double x)
{
    if (fabs(x) < 1e-10)
    {
        return (x >= 0) ? -1e20 : 1e20;
    }
    return -3.0 / (x * x);
}

