/* ==================================================================================
 * Функция: init_ncurses
 * ==================================================================================
 * Назначение:
 *   Выполняет полную инициализацию библиотеки ncurses для работы с консольным
 *   интерфейсом. Настраивает режимы ввода, цветовую схему и состояние курсора.
 *
 * Параметры:
 *   Нет (void)
 *
 * Возвращаемое значение:
 *   Нет (void)
 *
 * Особенности:
 *   - initscr() — инициализирует экран и создаёт окно stdscr
 *   - cbreak() — отключает буферизацию ввода (символы доступны сразу)
 *   - noecho() — отключает эхо-отображение вводимых символов
 *   - keypad(stdscr, TRUE) — активирует обработку специальных клавиш
 *     (стрелки, Enter, Esc, F1-F12 и т.д.)
 *   - start_color() — включает поддержку цветов
 *   - init_pair() — создаёт 4 цветовые пары для выделения элементов интерфейса:
 *       Pair 1: Чёрный текст на белом фоне (выделение пунктов меню)
 *       Pair 2: Белый текст на синем фоне (заголовки окон)
 *       Pair 3: Зелёный текст на чёрном фоне (строка статуса)
 *       Pair 4: Красный текст на чёрном фоне (сообщения об ошибках)
 *   - curs_set(0) — скрывает системный курсор терминала
 *
 * Примечание:
 *   Должна вызываться один раз в начале программы перед любыми другими
 *   вызовами функций ncurses.
 * ================================================================================== */
void init_ncurses(void)
{
    initscr();
    cbreak();
    noecho();
    keypad(stdscr, TRUE); // Включаем обработку спецклавиш для stdscr
    start_color();
    init_pair(1, COLOR_BLACK, COLOR_WHITE); // Выделение
    init_pair(2, COLOR_WHITE, COLOR_BLUE);  // Заголовки
    init_pair(3, COLOR_GREEN, COLOR_BLACK); // Статус
    init_pair(4, COLOR_RED, COLOR_BLACK);   // Ошибки
    curs_set(0);                            // Скрыть курсор
}

/* ==================================================================================
 * Функция: end_ncurses
 * ==================================================================================
 * Назначение:
 *   Корректно завершает работу с библиотекой ncurses, восстанавливая исходное
 *   состояние терминала и освобождая все выделенные ресурсы.
 *
 * Параметры:
 *   Нет (void)
 *
 * Возвращаемое значение:
 *   Нет (void)
 *
 * Особенности:
 *   - endwin() — восстанавливает режимы терминала, показывает курсор,
 *     очищает экран и возвращает управление стандартному выводу
 *
 * Примечание:
 *   Обязательно должна вызываться перед завершением программы, иначе
 *   терминал может остаться в некорректном состоянии (скрытый курсор,
 *   изменённые цвета, отключенное эхо и т.д.).
 * ================================================================================== */
void end_ncurses(void)
{
    endwin();
}

